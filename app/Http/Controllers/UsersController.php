    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        
        //Check if post exists before deleting
        if (!isset($user)){
            return redirect('/allusers')->with('error', 'No User Found');
        }

        // Check for correct user
        if(auth()->user()->id !==$user->id){
            return redirect('/allusers')->with('error', 'Unauthorized Page');
        }

        if($user->photo != 'noimage.jpg'){
            // Delete Image
            Storage::delete('public/cover_images/'.$user->photo);
        }
        
        $user->delete();
        return redirect('/allusers')->with('success', 'User Removed');
    }
